package extension.data;

import javax.xml.bind.annotation.XmlAttribute;

public class DActionChild {
//	<act id="" n="" e="" s="" d=""/>
	private int id;
	private int name;
	private int eff;
	private int send;
	private String desc;
	@XmlAttribute(name="id")
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	@XmlAttribute(name="n")
	public int getName() {
		return name;
	}
	public void setName(int name) {
		this.name = name;
	}
	@XmlAttribute(name="e")
	public int getEff() {
		return eff;
	}
	public void setEff(int eff) {
		this.eff = eff;
	}
	@XmlAttribute(name="s")
	public int getSend() {
		return send;
	}
	public void setSend(int send) {
		this.send = send;
	}
	@XmlAttribute(name="d")
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	
}
