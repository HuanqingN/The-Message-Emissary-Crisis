package extension.data;

import javax.xml.bind.annotation.XmlAttribute;

public class DCardsChild {
//	<ca id="1" co="1" tr="3"/>
	private int id;
	private int color;
	private int send;
	@XmlAttribute(name="id")
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	@XmlAttribute(name="co")
	public int getColor() {
		return color;
	}
	public void setColor(int color) {
		this.color = color;
	}
	@XmlAttribute(name="tr")
	public int getSend() {
		return send;
	}
	public void setSend(int send) {
		this.send = send;
	}
}
